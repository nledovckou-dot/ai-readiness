// Красная зона: высокая острота, низкая готовность
export function isRedZone(acuity: number, tech: number, org: number) {
  return acuity >= 4 && (tech <= 2 || org <= 2);
}

export function calcWeightedTech(scores: Record<string, number>, criteria: TechCriterion[]) {
  let total = 0;
  let usedWeight = 0;
  for (const c of criteria) {
    const v = scores[c.id];
    if (typeof v === "number") {
      total += v * c.weight;
      usedWeight += c.weight;
    }
  }
  if (usedWeight === 0) return 0;
  // Нормировка на случай, если что-то ещё не оценено
  return total / usedWeight;
}

export function calcFinal(acuity: number, tech: number, org: number) {
  return acuity * weights.acuity + tech * weights.tech + org * weights.org;
}
